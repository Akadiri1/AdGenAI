/**
 * Patches src/lib/avatars.ts to use the R2-hosted portraits from actor-portraits.json.
 *
 * Strategy: switch the av() helper so each actor's URL is read from a lookup map.
 * Falls back to randomuser.me for any actor not yet generated.
 */
import fs from "node:fs/promises";
import path from "node:path";

const mappingFile = path.resolve("scripts/actor-portraits.json");
const avatarsFile = path.resolve("src/lib/avatars.ts");

let mapping;
try { mapping = JSON.parse(await fs.readFile(mappingFile, "utf8")); }
catch { console.error("❌ scripts/actor-portraits.json not found. Run generate-actor-portraits.mjs first."); process.exit(1); }

const validUrls = Object.fromEntries(
  Object.entries(mapping).filter(([, v]) => typeof v === "string"),
);
const count = Object.keys(validUrls).length;
if (count === 0) { console.error("❌ No successful generations in mapping."); process.exit(1); }

let src = await fs.readFile(avatarsFile, "utf8");

// Replace the helper's URL composition. We add a lookup map at module top.
const startMarker = "// PORTRAIT_OVERRIDES_START";
const endMarker = "// PORTRAIT_OVERRIDES_END";
const block =
`${startMarker}
// Auto-generated by scripts/apply-actor-portraits.mjs — do not edit by hand.
const PORTRAIT_OVERRIDES: Record<string, string> = ${JSON.stringify(validUrls, null, 2)};
${endMarker}`;

if (src.includes(startMarker)) {
  src = src.replace(new RegExp(`${startMarker}[\\s\\S]*?${endMarker}`), block);
} else {
  // Insert just before the av() function
  src = src.replace(
    /\/\/ Helper to keep entries terse/,
    `${block}\n\n// Helper to keep entries terse`,
  );
}

// Patch the av() function to consult the override map
const oldAv = `  const thumbnailUrl = portrait.startsWith("photo-")
    ? \`https://images.unsplash.com/\${portrait}?w=400&h=400&fit=crop&crop=faces\`
    : \`https://randomuser.me/api/portraits/\${portrait}.jpg\`;`;
const newAv = `  const thumbnailUrl = PORTRAIT_OVERRIDES[id] ?? (portrait.startsWith("photo-")
    ? \`https://images.unsplash.com/\${portrait}?w=400&h=400&fit=crop&crop=faces\`
    : \`https://randomuser.me/api/portraits/\${portrait}.jpg\`);`;

if (!src.includes(newAv)) {
  if (!src.includes(oldAv)) {
    console.error("❌ Couldn't find the av() helper body to patch. Edit avatars.ts manually.");
    process.exit(1);
  }
  src = src.replace(oldAv, newAv);
}

await fs.writeFile(avatarsFile, src);
console.log(`✅ Patched ${avatarsFile} with ${count} R2 portraits.`);
console.log(`Actors not yet generated will keep their randomuser.me fallback.`);
